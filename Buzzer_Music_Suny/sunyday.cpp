  void BuzzerPin(int pin) {
    buzzerPin = pin;
}
// 播放单个音符的函数
void PlayTone(int prescaler, int duration_ms, int volume) 
{
    // 休止符处理
    if (prescaler == 30) 
    {
        noTone(buzzerPin);
        delay(duration_ms);
        return;
    }
    
    // 计算频率：使用定时器预分频值计算
    // 假设原始系统使用72MHz时钟，预分频值对应频率 = 72000000 / (prescaler * 100)
    // 因为自动重装值是100（Period=100-1），所以PWM频率 = 72MHz / (prescaler * 100)
    // 频率 = 720000 / prescaler (kHz)
    float frequency = 720000.0 / prescaler;
    
    // 播放音调
    if (volume > 0) 
    {
        tone(buzzerPin, frequency, duration_ms);
    } 
    else 
    {
        // 音量为0时不播放
        noTone(buzzerPin);
    }
    
    // 等待音符播放完成
    delay(duration_ms);
}

// 主音乐播放函数
void B_Music(void) {

   pinMode(buzzerPin, OUTPUT);
    // 计算数组长度
    int tone_count = sizeof(a) / sizeof(a[0]);
    int time_count = sizeof(tm) / sizeof(tm[0]);
    // 遍历播放所有音符
    for(int i = 0; i < tone_count; i++) 
    {
        // 设置默认音量（1表示播放，0表示静音）
        int volume = 1;
        
        // 计算延时时间（保持原始算法）
        int j = tm[i] / 25;
        int time_delay = j * 180;
        
        // 特定索引设置静音（原始代码逻辑）
        if(i == 49 || i == 67 || i == 178) 
        {
            volume = 0;
        }
        
        // 播放音符
        PlayTone(a[i], time_delay, volume);
    }
    
    // 播放结束，停止输出
    noTone(buzzerPin);
}
